-- Q27.Calculate the revenue share of each customer region compared to the total revenue and display only the regions whose share is greater than 25%.
 WITH region_share AS (
    SELECT customer_region,
           SUM(total_revenue) / 
           (SELECT SUM(total_revenue) FROM ecommerce_sales) AS share
    FROM ecommerce_sales
    GROUP BY customer_region
)
SELECT *
FROM region_share
WHERE share > 0.25; 